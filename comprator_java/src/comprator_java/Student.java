package comprator_java;

public class Student {
	private String name;
	private int age;
	private int roll;
	
	Student(String name,int age,int roll){
		this.name=name;
		this.age=age;
		this.roll=roll;
	}

	public String getName() {
		return name;
	}


	public int getAge() {
		return age;
	}


	public int getRoll() {
		return roll;
	}

}
